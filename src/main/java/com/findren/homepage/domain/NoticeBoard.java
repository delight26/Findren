package com.findren.homepage.domain;

public class NoticeBoard {
	private int notice_No;
	private String notice_Title;
	private String notice_Writer;
	private String notice_Content;
	private int notice_Watch;
	private String notice_IP;
	
	public int getNotice_No() {
		return notice_No;
	}
	public void setNotice_No(int notice_No) {
		this.notice_No = notice_No;
	}
	public String getNotice_Title() {
		return notice_Title;
	}
	public void setNotice_Title(String notice_Title) {
		this.notice_Title = notice_Title;
	}
	public String getNotice_Writer() {
		return notice_Writer;
	}
	public void setNotice_Writer(String notice_Writer) {
		this.notice_Writer = notice_Writer;
	}
	public String getNotice_Content() {
		return notice_Content;
	}
	public void setNotice_Content(String notice_Content) {
		this.notice_Content = notice_Content;
	}
	public int getNotice_Watch() {
		return notice_Watch;
	}
	public void setNotice_Watch(int notice_Watch) {
		this.notice_Watch = notice_Watch;
	}
	public String getNotice_IP() {
		return notice_IP;
	}
	public void setNotice_IP(String notice_IP) {
		this.notice_IP = notice_IP;
	}
	
}
